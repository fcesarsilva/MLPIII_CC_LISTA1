package br.unipe.mlp.lista1;

public class VeiculoDePasseio extends Veiculo{
	
	private int qtdPortas;
	private boolean arCondiciona;
	/**
	 * @return the qtdPortas
	 */
	public int getQtdPortas() {
		return qtdPortas;
	}
	/**
	 * @param qtdPortas the qtdPortas to set
	 */
	public void setQtdPortas(int qtdPortas) {
		this.qtdPortas = qtdPortas;
	}
	/**
	 * @return the arCondiciona
	 */
	public boolean isArCondiciona() {
		return arCondiciona;
	}
	/**
	 * @param arCondiciona the arCondiciona to set
	 */
	public void setArCondiciona(boolean arCondiciona) {
		this.arCondiciona = arCondiciona;
	}
	
	

}
