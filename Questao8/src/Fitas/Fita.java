package Fitas;

public class Fita {
	private String Titulo;
	private double Preco;

	public Fita(String titulo, double preco) {
		super();
		Titulo = titulo;
		Preco = preco;
	}
	public String getTitulo() {
		return Titulo;
	}
	public void setTitulo(String titulo) {
		Titulo = titulo;
	}
	public double getPreco() {
		return Preco;
	}
	public void setPreco(double preco) {
		Preco = preco;
	}

	
	
	

}
